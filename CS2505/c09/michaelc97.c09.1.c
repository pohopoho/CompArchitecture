/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 *
 *   First, remove all the bits except for the signed bit, then & it
 *   with 1 to check if negative or not. If 1, it is negative, then ! 
 *   that value to satisfy the function requirement, and vice versa 
 *   if that part returns 0
 */
int isNonNegative(int x) {
  return !((x >> 31) & 1);
}
/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   from lowbit to highbit, inclusive
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 *
 *   First, generate an int that is all 1's, then left shift 
 *   the number 2 by the highbit amount, then add it with the 
 *   all 1's value (x) to fill up 1's up to the highbit, then
 *   to remove the excess 1's on the right side, right shift it by 
 *   lowbit amount, then left shift it back to replace the excess
 *   1's with zeroes
 */
int bitMask(int highbit, int lowbit) {
  int x = ~0;
  return ((2 << highbit) + x) >> lowbit << lowbit;
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 *
 *   Create a value as a mask, then shift it to the proper position
 *   for replacement step. The left side creates x, but with the
 *   byte to be replaced with zero. On the right side, c is left
 *   shifted to the correct position, then both sides are | 
 *   together and returned
 */
int replaceByte(int x, int n, int c) {
  int b = 0xff;
  b = ~(b << (n << 3));

  return ((x & b) | (c << (n << 3)));
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 *
 *   Take the initial input x and remove the lower half and compare
 *   with the original to see in which spots only 1 exists (xor).
 *   Take this value and cut it inn half and repeat this comparison  
 *   through the iteration in which the value is only 1 bit in length
 *   when it is cut in half. Then if this value is 1, it contains an
 *   odd number of 0's.
 *
 */
int bitParity(int x) {
  int all = x ^ (x >> 16);
  int eight = all ^ (all >> 8);
  int four = eight ^ (eight >> 4);
  int two = four ^ (four >> 2);
  return 1 & (two ^ (two >> 1));
}
